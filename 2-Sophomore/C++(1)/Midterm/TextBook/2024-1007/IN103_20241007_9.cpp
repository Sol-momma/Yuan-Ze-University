#include <iostream>
#include <cmath>
using namespace std;

int main()
{
   cout << floor( 9.2 ) << endl; // 9
   cout << fabs( -9.2 ) << endl; // 9.2
   cout << log( 9.2 ) << endl; // natural logarithm
   cout << log10( 9.2 ) << endl; // logarithm with base 10
   cout << sin( 9.2 ) << endl; // sine
   cout << sqrt( 9.2 ) << endl; // square root
   cout << pow( 9.2, 8.2 ) << endl; // 9.2 raised to the power of 8.2
}



