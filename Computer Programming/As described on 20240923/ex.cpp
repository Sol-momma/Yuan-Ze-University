#include <iostream>
using namespace std;

int main()
{
    // 出力を行う
    cout << "こんにちは、世界！" << endl;
    cout << "C++の例文を表示しています。" << endl;
    cout << "今日はとても良い天気ですね。" << endl;

    // 数値の出力
    int a = 10;
    int b = 20;
    cout << "a の値は " << a << " です。" << endl;
    cout << "b の値は " << b << " です。" << endl;
    cout << "a + b の結果は " << (a + b) << " です。" << endl;

    return 0;
}
